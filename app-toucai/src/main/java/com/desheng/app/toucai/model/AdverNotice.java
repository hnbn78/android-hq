package com.desheng.app.toucai.model;

public class AdverNotice {
    public String title;
    public String url;

    public AdverNotice(String title) {
        this.title = title;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }
}
