package com.desheng.app.toucai.view;

public class AnimTextViewBean {
    String prefixt;
    String content;

    public AnimTextViewBean(String prefixt, String content) {
        this.prefixt = prefixt;
        this.content = content;
    }

    public String getPrefixt() {
        return prefixt;
    }
    public void setPrefixt(String prefixt) {
        this.prefixt = prefixt;
    }
    public String getContent() {
        return content;
    }
    public void setContent(String content) {
        this.content = content;
    }
}
